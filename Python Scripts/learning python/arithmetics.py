# Arithmetic operations
print(10+2)# Addition
print(10-2)# Substraction
print(10/2)#Division
print(11//2)# Division but returns value without remainder
print(10*2)# Multiplication
print(11%2)# Modulus
print(10**2)# Exponentiation

# You can also use comparism operators
print(10 > 10)
print(10 < 10)
print(10 >= 10)
print(10 <= 10)
print(10 == 10)
print(10 != 10)
