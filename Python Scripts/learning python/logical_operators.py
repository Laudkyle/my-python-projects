# Logical operators
# "and" ensures that every condition is true before it returns "True"
print("AND")
print()
print((10 > 5) and (1<4), " When all the condition are met")
print((10 < 5) and (1<4), "When one of the conditions is met")
# "or" checks if at least one of the conditions is met before it return "True"
print()
print("OR")
print()
print((10 < 5) or (1>4)," When no condition is met")
print((10 < 5) or (1<4), " When at least one condition is met")
# You can reverse the expression using "not"

print(not("James" == "joe"))# The expression has been reversed